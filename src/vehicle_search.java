    private void tblowner_detailsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblowner_detailsMouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
        pnlowner_details.setVisible(true);
        pnlvehicle_details1.setVisible(true);
        btnupdate.setVisible(true);
        btnback2.setVisible(true);
        btnupdate.setVisible(true);
        btnExit.setVisible(true);        
        pnltable.setVisible(false);
       
       
        //cust_regno = Integer.valueOf(tblcustomer_details.getValueAt(tblcustomer_details.getSelectedRow(), 0).toString());
       reg_no = Integer.valueOf(tblowner_details.getValueAt(tblowner_details.getSelectedRow(), 0).toString());
       txtreg_no.setText(String.valueOf(reg_no ));
       txtname.requestFocus();
       
       try
        {
            Statement st=con.createStatement();
            String no=txtreg_no.getText();

            String sql="SELECT * FROM owner_details WHERE reg_no='"+no+"'";
            ResultSet rs=st.executeQuery(sql);

            if(rs.next()==false)
            {
                JOptionPane.showMessageDialog(null, "Invalid rgister number");
            }
            String name=rs.getString("owner_name");
            txtname.setText(name);

            String address=rs.getString("address");
            txtaddress.setText(address);
            
             String nic=rs.getString("nic");
            txtnic.setText(nic);
            
            int phone =rs.getInt("phone");
            txtphone.setText(String.valueOf(phone));

            
            
         
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
       
        
    
        }
    }//GEN-LAST:event_tblowner_detailsMouseClicked

