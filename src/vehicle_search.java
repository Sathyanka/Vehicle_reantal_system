    private void load_owner_table() {
     try {
            
            Statement st=con.createStatement();
           
            DefaultTableModel table_model= (DefaultTableModel)tblowner_details.getModel();
         
            String sql="SELECT owner_details.reg_no,owner_details.owner_name,owner_details.nic,owner_details.address,owner_details.phone FROM owner_details  ORDER BY owner_details.reg_no DESC";
           ResultSet rs=st.executeQuery(sql);
            
            table_model.setRowCount(0);
            while (rs.next()) {
                ArrayList arrList = new ArrayList();
                arrList.add(rs.getString("reg_no"));
                arrList.add(rs.getString("owner_name"));
                arrList.add(rs.getString("nic"));
                arrList.add(rs.getString("address"));
                arrList.add(rs.getString("phone"));
                
                
                table_model.addRow(arrList.toArray());
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
     }
     

