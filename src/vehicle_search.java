    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
       
        String reg=txtreg_no.getText();
        String name=txtname.getText();
        String address=txtaddress.getText();
        String nic=txtnic.getText();
        String phone=txtphone.getText();
        
        String sql="UPDATE owner_details SET reg_no=?,owner_name=?,address=?,phone=?,nic=? WHERE rg_no='"+reg+"'";

        try{
            pst=con.prepareStatement(sql);
            pst.setString(1,txtreg_no.getText());
            pst.setString(2,txtname.getText());
            pst.setString(3,txtaddress.getText());
            pst.setString(4,txtphone.getText());
            pst.setString(5,txtnic.getText());

        }

        catch (Exception e) {

            JOptionPane.showMessageDialog(null,"succssesffful");
        }

    }//GEN-LAST:event_btnupdateActionPerformed

